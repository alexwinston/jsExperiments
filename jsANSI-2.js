class Polygon extends Object {
    constructor(x, y, width, height, color, backgroundColor) {
        super(x, y, color, backgroundColor)
        this.width = width - 1
        this.height = height - 1
        this.angle = 0
        this.points = []
        this.dirty = true
    }

    get width() {
        return this._width * this.scale;
    }
    set width(width) {
        if (this._width == width)
            return
        this.dirty = true
        this._width = width
    }

    get height() {
        return this._height * this.scale;
    }
    set height(height) {
        if (this._height == height)
            return
        this.dirty = true
        this._height = height
    }

    get angle() {
        return this._angle
    }
    set angle(angle) {
        if (this._angle == angle)
            return
        this.dirty = true
        this._angle = angle
    }

    radians(degrees) {
        return degrees * (Math.PI / 180);
    }

    rotatePoint(cx, cy, angle, p) {
        const s = Math.sin(angle);
        const c = Math.cos(angle);

        // translate point back to origin:
        p.x -= cx;
        p.y -= cy;

        // rotate point
        const xnew = p.x * c - p.y * s;
        const ynew = p.x * s + p.y * c;

        // translate point back:
        p.x = Math.round(xnew + cx);
        p.y = Math.round(ynew + cy);
        return p;
    }

    // https://classic.csunplugged.org/wp-content/uploads/2014/12/Lines.pdf
    line(x, y, x1, y1) {
        this.cachePoint(x, y)
        const dx = Math.abs(x1 - x),
            dy = Math.abs(y1 - y),
            sx = x < x1 ? 1 : -1,
            sy = y < y1 ? 1 : -1
        let err = dx - dy;

        while (x != x1 || y != y1) {
            const e2 = 2 * err;
            if (e2 > (dy * -1)) {
                err -= dy;
                x += sx;
            }
            if (e2 < dx) {
                err += dx;
                y += sy;
            }

            this.cachePoint(x, y)
        }
    }

    draw(ansi) {
        const points = this.points
        
        if (this.dirty) {
            this.update()

            const radians = this.radians(this.angle)

            const centerX = this.x + (this.width / 2)
            const centerY = this.y + (this.height / 2)

            for (let i = 0; i < points.length; i++) {
                this.rotatePoint(centerX, centerY, radians, points[i])
            }

            let minX = Number.MAX_VALUE;
            let maxX = -Number.MAX_VALUE;
            let minY = Number.MAX_VALUE;
            let maxY = -Number.MAX_VALUE;

            for (let i = 0; i < points.length; i++) {
                const point1 = points[i]
                const point2 = points[i == points.length - 1 ? 0 : i + 1]
                this.line(point1.x, point1.y, point2.x, point2.y)

                const x = point1.x;
                const y = point1.y;
                minX = Math.min(minX, x);
                maxX = Math.max(maxX, x);
                minY = Math.min(minY, y);
                maxY = Math.max(maxY, y); 
            }

            // console.log(this.pointCache)
    
            const width = maxX - minX;
            const height = maxY - minY;

            this.bounds.x = minX
            this.bounds.y = minY
            this.bounds.width = width
            this.bounds.height = height

            const x = Math.round(minX + (width / 2))
            const y = Math.round(minY + (height / 2))
            // console.log(this.bounds)
            // console.log(x, y)
            for (let i = this.bounds.x + 1; i < this.bounds.x + this.bounds.width; i++) {
                for (let j = this.bounds.y + 1; j < this.bounds.y + this.bounds.height; j++) {
                    if (this.isPointInPoly(this.points, {x:i, y:j}) && !this.pointCache.get(i).has(j)) {
                        this.fill(i,j)
                        i, j = Number.MAX_VALUE
                    }
                }
            }
            // this.fill(x, y)

            this.dirty = false
        }

        this.pointCache.forEach((columns, x) => {
            columns.forEach((charCode, y) => {
                this.renderPoint(ansi, charCode, x, y)
            })
        })
    }

    isPointInPoly(poly, pt){
        for(var c = false, i = -1, l = poly.length, j = l - 1; ++i < l; j = i)
            ((poly[i].y <= pt.y && pt.y < poly[j].y) || (poly[j].y <= pt.y && pt.y < poly[i].y))
            && (pt.x < (poly[j].x - poly[i].x) * (pt.y - poly[i].y) / (poly[j].y - poly[i].y) + poly[i].x)
            && (c = !c);
        return c;
    }
}

class Line extends Polygon {
    constructor(x, y, x2, y2, color, backgroundColor) {
        super(x, y, Math.abs(x2 - x), Math.abs(y2 - y), color, backgroundColor)
        this.x2 = x2
        this.y2 = y2

        this.update()
    }

    update() {
        const x = this.x, y = this.y
        const x2 = this.x2, y2 = this.y2

        const points = this.points
        points[0] = { x: x, y: y }
        points[1] = { x: x2, y: y2 }
    }
}

