class Ansi {
    // canvas  = new Canvas(128, 42);
    canvas  = new Canvas(30, 4);
    context = this.canvas.getContext('2d');

    imageData = this.context.getImageData(0, 0, this.canvas.width, this.canvas.height);
    buf = new ArrayBuffer(this.imageData.data.length);
    buf8 = new Uint8ClampedArray(this.buf);
    data = new Uint32Array(this.buf);

    constructor() {
    }

    resize() {
        var width = this.canvas.width;
        var height = this.canvas.height;

        // get the max size that fits both width and height by finding the min scale
        var canvasScale = Math.min(innerWidth / width, innerHeight / height);
        // or for max size that fills
        // var canvasScale = Math.max(innerWidth / width, innerHeight / height);
    
        // now set canvas size and resolution to the new scale
        this.canvas.style.width = Math.floor(width * canvasScale) - 20 + "px";
        this.canvas.style.height = Math.floor(height * canvasScale) - 20 + "px";
    }

    text(str, x, y, color, bg) {
        for (var i = 0; i < str.length; i++) {
            var char = str.charCodeAt(i) & 0xff
            this.render(char, x++, y, color, bg)
        }
    }

    // https://classic.csunplugged.org/wp-content/uploads/2014/12/Lines.pdf
    line(x, y, x1, y1) {
        this.renderCache.length = 0

        this.renderPoint(x, y)
        var dx = Math.abs(x1 - x),
            dy = Math.abs(y1 - y),
            sx = x < x1 ? 1 : -1,
            sy = y < y1 ? 1 : -1,
            err = dx - dy;

        while (x != x1 || y != y1) {
            var e2 = 2 * err;
            if (e2 > (dy * -1)) {
                err -= dy;
                x += sx;
            }
            if (e2 < dx) {
                err += dx;
                y += sy;
            }

            this.renderPoint(x, y)
            // console.log(x + ',' + y)
        }
    }

    renderCache = []

    renderPoint(x, y, color = rgba(0,0,0,1), bg = rgba(255,255,255,1)) {
        var point = { x:x, y:y }
        this.renderCache.push(point)
        var even = y%2==0
        // var char = even ? 220 : 223
        var char = even ?
            (this.renderCache.some(i => i.x==x && i.y==point.y + 1) ? 219 : 223) :
            (this.renderCache.some(i => i.x==x && i.y==point.y - 1) ? 219 : 220)
        this.render(char, x, Math.floor(point.y/2), color, bg)
    }

    render(char, x, y, color, bg) {
        var charcode = font[char]
        this.renderChar(charcode, x, y, color, bg);
    }

    // https://hacks.mozilla.org/2011/12/faster-canvas-pixel-manipulation-with-typed-arrays/
    draw() {
        this.imageData.data.set(this.buf8);
        this.context.putImageData(this.imageData, 0, 0);
    }

    renderChar(charcode, x, y, fg, bg) {
        var bits, row, col;

        var data = this.data;
        var charY = 0;
        for (row = (y*16)+0; row < (y*16)+16; row++) {
            bits = charcode[charY++] || 0x00;
            for (col = (x*8)+7; col >= (x*8)+0; col--) {
                data[row * this.canvas.width + col] = bits & 1 ? fg : bg
                bits >>= 1;
            }
        }
    }

    distance(p1, p2) {
        var dx = p2.x - p1.x; dx *= dx;
        var dy = p2.y - p1.y; dy *= dy;
        return Math.sqrt( dx + dy );
     }
     
     fillCircle(x, y, r) {
         var ret = [];
         for (var j=x-r; j<=x+r; j++)
            for (var k=y-r; k<=y+r; k++)
                if (this.distance({x:j,y:k},{x:x,y:y}) <= r) ret.push({x:j,y:k});
        //  console.log(ret)
         return ret;
     }

     circle(x_centre, y_centre, r) {
        this.renderCache.size = 0

        var x = r, y = 0;
     
        // Printing the initial point
        // on the axes after translation
        this.renderPoint(x + x_centre, y + y_centre);
     
        // When radius is zero only a single
        // point will be printed
        if (r > 0) {
            this.renderPoint(-x + x_centre, y + y_centre);
            this.renderPoint(x_centre, -x + y_centre);
            this.renderPoint(x_centre, x + y_centre);
        }
     
        // Initialising the value of P
        var P = 1 - r;
        while (x > y) {
             
            y++;
         
            // Mid-point is inside or on the perimeter
            if (P <= 0)
                P = P + 2 * y + 1;
         
            // Mid-point is outside the perimeter
            else {
                x--;
                P = P + 2 * y - 2 * x + 1;
            }
         
            // All the perimeter points have already
            // been printed
            if (x < y)
                break;
         
            // Printing the generated point and its
            // reflection in the other octants after
            // translation
            this.renderPoint(x + x_centre, y + y_centre);
            this.renderPoint(-x + x_centre, y + y_centre);
            this.renderPoint(x + x_centre, -y + y_centre);
            this.renderPoint(-x + x_centre, -y + y_centre);
         
            // If the generated point is on the
            // line x = y then the perimeter points
            // have already been printed
            if (x != y) {
                this.renderPoint(y + x_centre, x + y_centre);
                this.renderPoint(-y + x_centre, x + y_centre);
                this.renderPoint(y + x_centre, -x + y_centre);
                this.renderPoint(-y + x_centre, -x + y_centre);
            }
        }
    }
}

